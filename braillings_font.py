CHAR_H = 8
CHAR_GAP = 2


def text_width(text):
    """Width in pixels of rendered text."""
    w = 0
    for ch in text:
        lookup = ch.upper() if ch.lower() != ch.upper() else ch
        g = FONT.get(lookup)
        w += (len(g[0]) if g else 5) + CHAR_GAP
    return w - CHAR_GAP if w > 0 else 0


def stamp_text(world, x, y, text, color):
    """Stamp text into world as solid terrain. Returns set of (px, py) text pixel positions."""
    pixels = set()
    cx = x
    for ch in text:
        lookup = ch.upper() if ch.lower() != ch.upper() else ch
        glyph = FONT.get(lookup)
        if glyph is None:
            cx += 5 + CHAR_GAP
            continue
        for gy, row in enumerate(glyph):
            for gx, pixel in enumerate(row):
                if pixel == 'X':
                    px, py = cx + gx, y + gy
                    world.set(px, py, color)
                    pixels.add((px, py))
        cx += len(glyph[0]) + CHAR_GAP
    return pixels
